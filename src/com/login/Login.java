package com.login;

public class Login {
	
	private String id_, pw;

	public Login(String id_, String pw) {
		this.id_ = id_;
		this.pw = pw;
	}

	public String getId_() {
		return id_;
	}

	public void setId_(String id_) {
		this.id_ = id_;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}
	
	
	
	

}
